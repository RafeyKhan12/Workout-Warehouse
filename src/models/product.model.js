import mongoose from "mongoose";

const productSchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true,
    },
    description: {
        type: String,
        required: true
    },
    price: {
        type: Number,
        required: true,
        min: 0
    },
    stock: {
        type: Number,
        required: true,
        min: 0
    },
    productImg: {
        type: String,
        required: true
    }
}, {timestamps: true});

export const Product = mongoose.models.Product || mongoose.model("Product", productSchema);